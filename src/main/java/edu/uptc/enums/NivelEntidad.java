package edu.uptc.enums;

/**
 * Enumeración que define los niveles de una entidad.
 * Puede ser MUNICIPAL, DEPARTAMENTAL o NACIONAL.
 *
 * @author Jennifer, Jesus y Santiago
 * @version 1.0
 */
public enum NivelEntidad {
    /** Representa una entidad de nivel municipal. */
    MUNICIPAL,
    /** Representa una entidad de nivel departamental. */
    DEPARTAMENTAL,
    /** Representa una entidad de nivel nacional. */
    NACIONAL
}
